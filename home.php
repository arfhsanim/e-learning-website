  <h4>Course Rationale</h4> 
  <p>Software Engineering is one of the fundamental courses for CSE students. It aims to
give students the practical idea about software engineering and analytical skills to use
in their subsequent course work and professional development. Discusses several
methods and principles of quality assurance and concept on professional software
development. <br>
The course is designed to provide the background of the following topics:
<br> <br>
a) To provide a comprehensive introduction to software engineering leading to
the ability to understand contemporary terminology, progress, issues, and
trends. <br>
b) A thorough introduction to software engineering techniques, focusing on
software process models, agile development. Principle that guides practice,
understanding requirements, Requirements modeling: scenarios, information
and analysis classes, Requirements modeling: Flow, behavior, patterns and
web apps, Design concept, pattern-based design, quality management,
software quality assurance, software testing strategies. <br>
c) The interdisciplinary nature of software engineering is emphasized in the wide
variety of examples and applications. To build up logical thinking.
</p>



 

